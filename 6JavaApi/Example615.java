public class Example615
{
	public static void main(String[] args){
		System.out.println("计算绝对值的结果："+Math.abs(-1));
		System.out.println("大于参数的最小整数："+Math.ceil(5.6));
		System.out.println("小于参数的最大整数："+Math.floor(-4.2));
		System.out.println("对小数进行四舍五入的结果："+Math.round(-4.6));
		System.out.println("两个数的最大值："+Math.max(2.1,-2.1));
		System.out.println("两个数的最小值："+Math.min(2.1,-2.1));
		System.out.println("生成一个大于等于0.0小于1.0的随机数"+Math.random());
	}
}
